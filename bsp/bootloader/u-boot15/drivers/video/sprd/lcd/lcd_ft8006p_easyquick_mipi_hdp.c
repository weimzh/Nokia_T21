static void tps65132_init(void)
{
	unsigned char cmd = 0x00;
	unsigned char data = 0x12;
	unsigned char cmd1 = 0x01;
	unsigned char data1 = 0x12;

	i2c_set_bus_num(CONFIG_TPS_I2C_BUS);
	i2c_init(CONFIG_TPS_I2C_SPEED, TPS65132_SLAVE_ADDR_WRITE);
	sprd_gpio_request(NULL, CONFIG_TPS_GPIO_EN);
	sprd_gpio_direction_output(NULL, CONFIG_TPS_GPIO_EN, 1);
	mdelay(50);

	tps65132_write_reg(cmd, data);
	tps65132_write_reg(cmd1, data1);
	mdelay(10);
}

