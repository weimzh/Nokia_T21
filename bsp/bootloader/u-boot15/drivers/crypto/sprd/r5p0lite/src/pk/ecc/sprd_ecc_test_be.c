static int ecc_verify_test(sprd_ecc_curveid_t curve_id)
{
	sprd_ecc_pubkey_t     ecc_public_key;
	sprd_ecc_signature_t  rs;
	struct ecc_test_st    *test_data = &ecc_test_vector_be[curve_id];
	uint32_t              temp = 0;
	int                   ret = 0;

	if (ecc_test_vector_be[curve_id].ecc_curve != curve_id) {
		SPRD_CRYPTO_LOG_TRACE("ecc_verify_test no match curve, id = (%d)\n", curve_id);
		return -1;
	}

	ret |= sprd_set_ecc_publickey(curve_id, test_data->ecc_pxy, test_data->pxy_bytelen, &ecc_public_key);
	ret |= sprd_set_ecc_signature(curve_id, test_data->ecc_rs, test_data->rs_bytelen, &rs);
	ret |= sprd_ecc_verify(curve_id, &ecc_public_key, (uint8_t *)test_data->ecc_mhash, test_data->mhash_bytelen, &rs);
	if (ret) {
		SPRD_CRYPTO_LOG_TRACE("ecc_verify_test fail 1, id = (%d) ret = (%x)\n", curve_id, ret);
		return -2;
	} else {
		SPRD_CRYPTO_LOG_TRACE("ecc_verify_test pass 1, id = (%d) ret = (%x)\n", curve_id, ret);
	}

	temp = rs.ecc_s[0];
	rs.ecc_s[0] = 0;
	ret = sprd_ecc_verify(curve_id, &ecc_public_key, (uint8_t *)test_data->ecc_mhash, test_data->mhash_bytelen, &rs);
	if (ret) {
		SPRD_CRYPTO_LOG_TRACE("ecc_verify_test pass2, id = (%d) ret = (%x)\n", curve_id, ret);
	} else {
		SPRD_CRYPTO_LOG_TRACE("ecc_verify_test fail2, id = (%d) ret = (%x)\n", curve_id, ret);
		return -3;
	}


	rs.ecc_s[0] = temp;
	ecc_public_key.pubkey_y[0] = 0;
	ret = sprd_ecc_verify(curve_id, &ecc_public_key, (uint8_t *)test_data->ecc_mhash, test_data->mhash_bytelen, &rs);
	if (ret) {
		SPRD_CRYPTO_LOG_TRACE("ecc_verify_test pass3, id = (%d) ret = (%x)\n", curve_id, ret);
	} else {
		SPRD_CRYPTO_LOG_TRACE("ecc_verify_test fail3, id = (%d) ret = (%x)\n", curve_id, ret);
		return -4;
	}
	return 0;
}

static int ecc_sign_test(sprd_ecc_curveid_t curve_id)
{
	struct ecc_test_st *test_data = &ecc_test_vector_be[curve_id];
	sprd_ecc_pubkey_t ecc_public_key;
	sprd_ecc_prikey_t ecc_private_key;
	sprd_ecc_signature_t rs;
	uint8_t signature[150];
	uint32_t s_len = 150;
	uint32_t temp;
	int ret = 0;


	if (ecc_test_vector_be[curve_id].ecc_curve != curve_id) {
		SPRD_CRYPTO_LOG_TRACE("ecc_sign_test no match curve, id = (%d)\n", curve_id);
		return -1;
	}


	ret |= sprd_set_ecc_privatekey(curve_id, test_data->ecc_d, test_data->d_bytelen, &ecc_private_key);
	ret |= sprd_set_ecc_publickey(curve_id, test_data->ecc_pxy, test_data->pxy_bytelen, &ecc_public_key);
	ret |= sprd_ecc_sign(curve_id, &ecc_private_key, (uint8_t *)test_data->ecc_mhash,test_data->mhash_bytelen, &rs);
	ret |= sprd_get_ecc_signature(&rs, signature, &s_len);
	sprd_pal_memset(rs.ecc_r, 0, test_data->rs_bytelen/2);
	sprd_pal_memset(rs.ecc_s, 0, test_data->rs_bytelen/2);
	ret |= sprd_set_ecc_signature(curve_id, signature, s_len, &rs);
	ret |= sprd_ecc_verify(curve_id, &ecc_public_key, (uint8_t *)test_data->ecc_mhash, test_data->mhash_bytelen, &rs);
	if (ret) {
		SPRD_CRYPTO_LOG_TRACE("ecc_sign_test fail, id = (%d) ret = (%x)\n", curve_id, ret);
		return -2;
	} else {
		SPRD_CRYPTO_LOG_TRACE("ecc_sign_test pass, id = (%d) ret = (%x)\n", curve_id, ret);
	}

	temp = rs.ecc_s[0];
	rs.ecc_s[0] = (temp^0xffffffff)+1;
	ret = sprd_ecc_verify(curve_id, &ecc_public_key, (uint8_t *)test_data->ecc_mhash, test_data->mhash_bytelen, &rs);
	if (ret) {
		SPRD_CRYPTO_LOG_TRACE("ecc_sign_test pass2, id = (%d) ret = (%x)\n", curve_id, ret);
	} else {
		SPRD_CRYPTO_LOG_TRACE("ecc_sign_test fail2, id = (%d) ret = (%x)\n", curve_id, ret);
		return -3;
	}

	rs.ecc_s[0] = temp;
	ecc_public_key.pubkey_y[0] = (ecc_public_key.pubkey_y[0]^0xffffffff)+1;
	ret = sprd_ecc_verify(curve_id, &ecc_public_key, (uint8_t *)test_data->ecc_mhash, test_data->mhash_bytelen, &rs);
	if (ret) {
		SPRD_CRYPTO_LOG_TRACE("ecc_sign_test pass3, id = (%d) ret = (%x)\n", curve_id, ret);
	} else {
		SPRD_CRYPTO_LOG_TRACE("ecc_sign_test fail3, id = (%d) ret = (%x)\n", curve_id, ret);
		return -4;
	}

	return 0;
}

